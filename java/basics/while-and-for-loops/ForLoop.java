/******************************************
 *  Compilation:  javac ForLoop.java
 *  Execution:    java ForLoop
 *
 *  Counting to five using a for loop.
 *
 ******************************************/

public class ForLoop {

	public static void main(String[] args) {
		// for loop for counting to 5
		for (int i = 1; i <= 5; i++) {
			System.out.println("The count is: " + i);
		}

		// print 'done!'
		System.out.println("Done!");
	}

}